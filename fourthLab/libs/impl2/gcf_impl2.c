#include "gcf.h"

// Реализация 2: Алгоритм Евклида
// Используем рекурсивный метод: НОД(A,B) = НОД(B, A mod B)
int GCF(int A, int B) {
    if (A <= 0 || B <= 0) {
        return -1; // Ошибка: числа должны быть натуральными
    }
    
    // Базовый случай
    if (B == 0) {
        return A;
    }
    
    // Рекурсивный вызов
    return GCF(B, A % B);
}
