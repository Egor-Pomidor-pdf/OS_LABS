#include "gcf.h"

// Реализация 1: Наивный алгоритм НОД
// Проходим от минимума чисел вниз и ищем первый общий делитель
int GCF(int A, int B) {
    if (A <= 0 || B <= 0) {
        return -1; // Ошибка: числа должны быть натуральными
    }
    
    int min = (A < B) ? A : B;
    
    for (int d = min; d >= 1; d--) {
        if (A % d == 0 && B % d == 0) {
            return d;
        }
    }
    
    return 1;
}
